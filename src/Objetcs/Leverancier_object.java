package Objetcs;

public class Leverancier_object {

	int lev_number;
	String lev_text;
	
	
	
	
	public Leverancier_object(int lev_number, String lev_text) {
		this.lev_number = lev_number;
		this.lev_text = lev_text;
	}
	
	public int getLev_number() {
		return lev_number;
	}

	public String getLev_text() {
		return lev_text;
	}

	
	
	
}
